class Rule() {
  private val _id: Long = 0
  private val _name: String = ""
  private val _ruleContent: RuleContent = null
  private val _version: String = ""
  private val _lastModified: String = ""
  private val _createTime: String = ""

  def id = _id
  def name = _name
  def ruleContent = _ruleContent
  def version = _version
  def lastModified = _lastModified
  def createTime = _createTime
}
